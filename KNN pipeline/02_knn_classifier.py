"""### 嘗試調整預測門檻以提升 MALIGNANT Recall
不直接使用 `predict()`，而是透過 `predict_proba()` 獲取機率
"""

import numpy as np
from sklearn.metrics import classification_report, confusion_matrix, accuracy_score

# 確保模型使用最佳參數重新訓練
knn_best = KNeighborsClassifier(n_neighbors=11)
knn_best.fit(X_train[features], y_train)

# 獲取預測機率 [P(BENIGN), P(MALIGNANT)]
y_probs = knn_best.predict_proba(X_val[features])

# 找到 'MALIGNANT' 在 classes_ 中的索引
malignant_idx = np.where(knn_best.classes_ == 'MALIGNANT')[0][0]

# 設定一個較低的門檻，例如 0.4 (只要機率大於 40% 就判斷為惡性)
threshold = 0.4
y_pred_custom = np.where(y_probs[:, malignant_idx] >= threshold, 'MALIGNANT', 'BENIGN')

print(f"--- 門檻調整為 {threshold} 的結果 ---")
print(f"最終準確率 (Accuracy): {accuracy_score(y_val, y_pred_custom):.4f}")
print("\n詳細分類報告:")
print(classification_report(y_val, y_pred_custom))

print("\n混淆矩陣:")
print(confusion_matrix(y_val, y_pred_custom))

