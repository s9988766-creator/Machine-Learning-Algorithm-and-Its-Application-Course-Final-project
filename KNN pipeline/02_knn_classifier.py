import pandas as pd
from sklearn.model_selection import cross_val_score
from sklearn.neighbors import KNeighborsClassifier
from sklearn.feature_selection import SequentialFeatureSelector
from sklearn.metrics import make_scorer, recall_score
import warnings
import time # Import the time module
warnings.filterwarnings('ignore')

# 1. 讀取你專屬的訓練集資料
train_df = pd.read_csv('train_scaled.csv')
X_train = train_df.drop(columns=['case', 'result'])
y_train = train_df['result']

# 🔥 關鍵核心：建立一個專門鎖定 "MALIGNANT" 的 Recall 評分器
recall_malignant_scorer = make_scorer(recall_score, pos_label='MALIGNANT')

best_overall_score = 0
best_k = 0
best_features = []

start_overall_time = time.time() # Record overall start time

for k in range(3, 16, 2):
    iteration_start_time = time.time() # Record iteration start time

    knn = KNeighborsClassifier(n_neighbors=k)

    sfs = SequentialFeatureSelector(
        knn,
        n_features_to_select=5,
        direction='forward',
        cv=5,
        scoring=recall_malignant_scorer
    )
    sfs.fit(X_train, y_train)

    selected_features = X_train.columns[sfs.get_support()].tolist()
    X_train_selected = X_train[selected_features]

    scores = cross_val_score(knn, X_train_selected, y_train, cv=5, scoring=recall_malignant_scorer)
    avg_score = scores.mean()

    iteration_end_time = time.time() # Record iteration end time
    iteration_duration = iteration_end_time - iteration_start_time

    print(f"當 k={k:2d} 時 | 5-Fold 平均 Recall: {avg_score:.4f} (耗時: {iteration_duration:.2f} 秒)")

    if avg_score > best_overall_score:
        best_overall_score = avg_score
        best_k = k
        best_features = selected_features

end_overall_time = time.time() # Record overall end time
total_elapsed_time = end_overall_time - start_overall_time

print("\n" + "="*50)
print(f"最佳 k 值: {best_k}")
print(f"最高 Recall: {best_overall_score:.4f} ({(best_overall_score*100):.2f}%)")
print(f"篩選出的特徵組合 ({len(best_features)}個):")
for feature in best_features:
    print(f" - {feature}")
print(f"總執行時間: {total_elapsed_time:.2f} 秒") # Print total elapsed time
print("="*50)

